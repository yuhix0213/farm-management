-- ============================================================
--  ① アプリ専用ユーザーを作成
--     '%' = すべてのホストから接続可（プレテスト用）
--     本番環境ではVercelのIPに絞ること
-- ============================================================
CREATE USER IF NOT EXISTS 'farm_app'@'%'
  IDENTIFIED BY 'testz01234';

-- ============================================================
--  ② farm_management DBへの権限付与（最小権限の原則）
--     DROP / CREATE / ALTER は付与しない
-- ============================================================
GRANT SELECT, INSERT, UPDATE, DELETE
  ON farm_management.*
  TO 'farm_app'@'%';

-- ============================================================
--  ③ 設定を即時反映
-- ============================================================
FLUSH PRIVILEGES;

-- ============================================================
--  ④ 確認（権限が正しく付与されているか確認する）
-- ============================================================
SHOW GRANTS FOR 'farm_app'@'%';

